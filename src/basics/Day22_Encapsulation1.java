package basics;

public class Day22_Encapsulation1 {
	
	private String model;
	private int imei;
	private String make;
	
	public String getModel() {
		return model;
	}
	
	public void setModel(String model) {
		this.model = model;
	}
	
	public int getImei() {
		return imei;
	}
	
	public void setImei(int imei) {
		this.imei = imei;
	}
	
	public String getMake() {
		return make;
	}
	
	public void setMake(String make) {
		this.make = make;
	}
	
	
}